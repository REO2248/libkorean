macro_rules! 자모_구현 {
    ($ty:ty, [$(($item:ident, $ch:expr)),+ $(,)?]) => {
        impl $ty {
            pub const fn 자모(self) -> char {
                match self {
                    $(Self::$item => $ch,)+
                }
            }

            pub const fn from_첫소리(c: char) -> Option<Self> {
                match c {
                    $($ch => Some(Self::$item),)+
                    _ => None,
                }
            }
        }
    };
}

macro_rules! 특성_구현 {
    ($ty:ty, $first_ch:expr) => {
        impl std::fmt::Display for $ty {
            fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
                write!(f, "{}", char::from(*self))
            }
        }

        impl TryFrom<char> for $ty {
            type Error = ();
            fn try_from(ch: char) -> Result<Self, Self::Error> {
                use std::convert::TryInto;
                (ch as u32).try_into()
            }
        }

        impl TryFrom<u32> for $ty {
            type Error = ();
            fn try_from(n: u32) -> Result<Self, Self::Error> {
                match n.checked_sub($first_ch as u32) {
                    Some(idx) => FromPrimitive::from_u32(idx).ok_or(()),
                    None => Err(()),
                }
            }
        }

        impl Serialize for $ty {
            fn serialize<S: serde::Serializer>(&self, serializer: S) -> Result<S::Ok, S::Error> {
                serializer.serialize_char((*self).into())
            }
        }

        impl<'de> Deserialize<'de> for $ty {
            fn deserialize<D: serde::Deserializer<'de>>(deserializer: D) -> Result<Self, D::Error> {
                use serde::de::Error;
                use std::convert::TryInto;
                let ch = char::deserialize(deserializer)?;
                ch.try_into()
                    .map_err(|_| D::Error::custom(format!("Not {}", stringify!($ty))))
            }
        }
    };
}

