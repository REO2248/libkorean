#[must_use]
pub fn 첫소리_소리마디로_변환(
    첫소리: char,
    가운데소리: char,
    jo: Option<char>,
) -> Option<String> {
    use crate::engine::{가운데소리, 끝소리, 첫소리};
    use std::convert::TryFrom;

    let c = 첫소리::try_from(첫소리)
        .ok()
        .or_else(|| 첫소리::from_첫소리(첫소리))?;
    let v = 가운데소리::try_from(가운데소리)
        .ok()
        .or_else(|| 가운데소리::from_첫소리(가운데소리))?;
    let t = if let Some(j) = jo {
        Some(
            끝소리::try_from(j)
                .ok()
                .or_else(|| 끝소리::from_첫소리(j))?,
        )
    } else {
        None
    };

    Some(c.조합(v, t))
}

#[must_use]
pub fn 소리마디를_첫소리로_변환(syl: char) -> Option<(char, char, Option<char>)> {
    if !소리마디인가(syl) {
        return None;
    }

    let offset = syl as u32 - 0xAC00;
    let jong_idx = offset % 28;
    let jung_idx = (offset / 28) % 21;
    let cho_idx = offset / (21 * 28);

    let 첫소리 = char::from_u32(0x1100 + cho_idx)?;
    let 가운데소리 = char::from_u32(0x1161 + jung_idx)?;
    let jo = if jong_idx != 0 {
        char::from_u32(0x11A7 + jong_idx)
    } else {
        None
    };

    Some((첫소리, 가운데소리, jo))
}

#[must_use]
pub fn decompose_string(input: &str) -> String {
    let mut output = String::new();
    for c in input.chars() {
        if let Some((첫소리, 가운데소리, jo)) = 소리마디를_첫소리로_변환(c) {
            output.push(첫소리);
            output.push(가운데소리);
            if let Some(j) = jo {
                output.push(j);
            }
        } else {
            output.push(c);
        }
    }
    output
}
